public class CodeSprint4_9AM{
	public static void main(String[] args){

		//Declare x
		int x = 3;
		//Enter While loop
		while(x <= 33){
			System.out.print(x + " ");
			x = x + 6;
		}
		System.out.println();
	}//end main
}//end class CodeSprint4_9AM